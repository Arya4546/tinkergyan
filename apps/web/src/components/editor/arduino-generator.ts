/**
 * arduino-generator.ts
 *
 * A Blockly CodeGenerator that emits valid Arduino C++ (a subset of C++11).
 * Handles both custom Arduino blocks and the standard Blockly library blocks
 * exposed in the toolbox (logic, loops, math, variables, procedures).
 *
 * Usage:
 *   import { arduinoGenerator } from './arduino-generator';
 *   const code = arduinoGenerator.workspaceToCode(workspace);
 */
import { CodeGenerator } from 'blockly/core';
import type { Block, Workspace } from 'blockly/core';

// ─── Operator precedence for C++ ─────────────────────────────────────────────
export enum Order {
  ATOMIC        = 0,   // Literals, identifiers, function calls
  UNARY         = 3,   // ! ~ - + (prefix)
  MULTIPLY      = 5,   // * / %
  ADD           = 6,   // + -
  RELATIONAL    = 9,   // < <= > >=
  EQUALITY      = 10,  // == !=
  LOGICAL_AND   = 14,  // &&
  LOGICAL_OR    = 15,  // ||
  ASSIGNMENT    = 17,  // = += -= etc.
  NONE          = 99,  // No parentheses needed
}

// ─────────────────────────────────────────────────────────────────────────────
// Generator instance
// ─────────────────────────────────────────────────────────────────────────────
export const arduinoGenerator = new CodeGenerator('Arduino');
arduinoGenerator.INDENT = '  ';

/**
 * Called after all blocks are processed. Prepends global variable declarations
 * collected during generation, then wraps with the Arduino boilerplate header.
 */
(arduinoGenerator as any).finish = function (code: string): string {
  // Collect variable declarations stored by variables_set blocks
  const varDecls = Object.values((this as any).definitions_ as Record<string, string>)
    .join('\n');
  const header = '// Generated by Tinkergyan Block Editor\n#include <Arduino.h>\n';
  return [header, varDecls ? varDecls + '\n' : '', code].filter(Boolean).join('\n');
};

/** Handle statement blocks that generate naked values (e.g. millis() as statement). */
(arduinoGenerator as any).scrubNakedValue = function (line: string): string {
  return `${line};\n`;
};

/**
 * Concatenate this block's code with the next connected statement block.
 * Mirrors the pattern used in Blockly's built-in generators.
 */
(arduinoGenerator as any).scrub_ = function (
  block: Block,
  code: string,
  thisOnly = false,
): string {
  const next = block.nextConnection?.targetBlock();
  const nextCode = !thisOnly && next ? (this as CodeGenerator).blockToCode(next) : '';
  return code + nextCode;
};

// ─────────────────────────────────────────────────────────────────────────────
// ARDUINO STRUCTURE BLOCKS
// ─────────────────────────────────────────────────────────────────────────────

arduinoGenerator.forBlock['arduino_program'] = function (block, generator) {
  const setup = generator.statementToCode(block, 'SETUP');
  const loop  = generator.statementToCode(block, 'LOOP');

  return [
    'void setup() {',
    setup || `${generator.INDENT}// empty`,
    '}',
    '',
    'void loop() {',
    loop || `${generator.INDENT}// empty`,
    '}',
    '',
  ].join('\n');
};

